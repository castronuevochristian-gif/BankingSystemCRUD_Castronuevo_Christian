    private void btnDepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositActionPerformed
   String customerId = txtSearchId.getText().trim();
        if (customerId.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a Customer ID first!");
            return;
        }

        String amountInput = txtAmount.getText().trim();
        if (amountInput.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter an amount to deposit!");
            return;
        }

        try {
            double amount = Double.parseDouble(amountInput);
            
            try (java.sql.Connection conn = DatabaseConnection.getConnection()) {
                // 1. Find the Account ID linked to this Customer ID
                String findAccSql = "SELECT account_id FROM accounts WHERE customer_id = ?";
                java.sql.PreparedStatement pstmtFind = conn.prepareStatement(findAccSql);
                pstmtFind.setString(1, customerId);
                java.sql.ResultSet rs = pstmtFind.executeQuery();
                
                if (rs.next()) {
                    int accountId = rs.getInt("account_id");
                    
                    // 2. Add money to balance using the CORRECT Account ID
                    String sqlUpdate = "UPDATE accounts SET balance = balance + ? WHERE account_id = ?";
                    java.sql.PreparedStatement pstmtUpdate = conn.prepareStatement(sqlUpdate);
                    pstmtUpdate.setDouble(1, amount);
                    pstmtUpdate.setInt(2, accountId);
                    pstmtUpdate.executeUpdate();
                    
                    // 3. Log transaction
                    String sqlLog = "INSERT INTO transactions (account_id, transaction_type, amount) VALUES (?, 'Deposit', ?)";
                    java.sql.PreparedStatement pstmtLog = conn.prepareStatement(sqlLog);
                    pstmtLog.setInt(1, accountId);
                    pstmtLog.setDouble(2, amount);
                    pstmtLog.executeUpdate();
                    
                    javax.swing.JOptionPane.showMessageDialog(this, "Deposit Successful!");
                    refreshBalance();
                    searchTransactions(customerId);
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "No account found for this Customer ID!");
                }
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid number format.");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDepositActionPerformed

    private void btnWithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWithdrawActionPerformed

    String customerId = txtSearchId.getText().trim();
        if (customerId.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a Customer ID first!");
            return;
        }

        try {
            double amount = Double.parseDouble(txtAmount.getText().trim());
            
            try (java.sql.Connection conn = DatabaseConnection.getConnection()) {
                // 1. Find the Account ID and current balance for this customer
                String sqlCheck = "SELECT account_id, balance FROM accounts WHERE customer_id = ?";
                java.sql.PreparedStatement pstmtCheck = conn.prepareStatement(sqlCheck);
                pstmtCheck.setString(1, customerId);
                java.sql.ResultSet rs = pstmtCheck.executeQuery();
                
                if (rs.next()) {
                    int accountId = rs.getInt("account_id");
                    double currentBalance = rs.getDouble("balance");
                    
                    if (currentBalance >= amount) {
                        // 2. Update balance using CORRECT Account ID
                        String sqlUpdate = "UPDATE accounts SET balance = balance - ? WHERE account_id = ?";
                        java.sql.PreparedStatement pstmtUpdate = conn.prepareStatement(sqlUpdate);
                        pstmtUpdate.setDouble(1, amount);
                        pstmtUpdate.setInt(2, accountId);
                        pstmtUpdate.executeUpdate();
                        
                        // 3. Log transaction
                        String sqlLog = "INSERT INTO transactions (account_id, transaction_type, amount) VALUES (?, 'Withdraw', ?)";
                        java.sql.PreparedStatement pstmtLog = conn.prepareStatement(sqlLog);
                        pstmtLog.setInt(1, accountId);
                        pstmtLog.setDouble(2, amount);
                        pstmtLog.executeUpdate();
                        
                        javax.swing.JOptionPane.showMessageDialog(this, "Withdrawal Successful!");
                        refreshBalance();
                        searchTransactions(customerId);
                    } else {
                        javax.swing.JOptionPane.showMessageDialog(this, "Insufficient Funds!");
                    }
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Account not found for this Customer ID!");
                }
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number");
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnWithdrawActionPerformed

