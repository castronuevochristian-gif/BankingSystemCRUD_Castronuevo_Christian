    private void refreshBalance() {
     String customerId = txtSearchId.getText().trim();
        if (customerId.isEmpty()) return;

        try (java.sql.Connection conn = DatabaseConnection.getConnection()) {
         
            String sql = "SELECT a.balance, c.first_name, c.last_name " +
                         "FROM accounts a " +
                         "JOIN customers c ON a.customer_id = c.customer_id " +
                         "WHERE a.customer_id = ?";
                         
            java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, customerId);
            java.sql.ResultSet rs = pstmt.executeQuery();
            
            if (rs.next()) {
              
                double balance = rs.getDouble("balance");
                String firstName = rs.getString("first_name");
                String lastName = rs.getString("last_name");
                
                // 3. Update both of your labels!
                lblBalance.setText("Balance: $" + String.format("%.2f", balance));
                lblName.setText("Customer: " + firstName + " " + lastName); 
            } else {
              
                lblBalance.setText("Account not found");
                lblName.setText(""); 
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
private void searchTransactions(String customerId) {
       
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblTransactions.getModel();
     
        
        
        model.setRowCount(0);

        String query = "SELECT t.* FROM transactions t " +
                       "JOIN accounts a ON t.account_id = a.account_id " +
                       "WHERE a.customer_id = ?";

        try (java.sql.Connection conn = DatabaseConnection.getConnection();
             java.sql.PreparedStatement pstmt = conn.prepareStatement(query)) {
            
            pstmt.setString(1, customerId);
            java.sql.ResultSet rs = pstmt.executeQuery();
            
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("transaction_id"), 
                    rs.getInt("account_id"), 
                    rs.getString("transaction_type"), 
                    rs.getDouble("amount"), 
                    rs.getTimestamp("transaction_date")
                });
            }
            
        } catch (Exception e) { 
            javax.swing.JOptionPane.showMessageDialog(this, "Error updating table: " + e.getMessage());
        }
    }

