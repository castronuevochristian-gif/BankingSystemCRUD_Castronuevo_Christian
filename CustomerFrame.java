    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    
      
        String firstName = txtFirstName.getText().trim();
        String lastName = txtLastName.getText().trim();
        String email = txtEmail.getText().trim();
        String phone = txtPhone.getText().trim();
        String initialBalanceStr = txtInitialBalance.getText().trim();
        
        
        
        if (firstName.isEmpty() || lastName.isEmpty() || email.isEmpty() || phone.isEmpty() || initialBalanceStr.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Please fill in all the required information. No fields can be left blank!", 
                "Missing Information", 
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return; 
        }

        try {
            
            double initialBalance = Double.parseDouble(initialBalanceStr);
            String accountType = cmbAccountType.getSelectedItem().toString();

            try (java.sql.Connection conn = DatabaseConnection.getConnection()) {
               
                String insertCustomer = "INSERT INTO customers (first_name, last_name, email, phone_number) VALUES (?, ?, ?, ?)";
                java.sql.PreparedStatement pstmtCust = conn.prepareStatement(insertCustomer, java.sql.Statement.RETURN_GENERATED_KEYS);
                pstmtCust.setString(1, firstName);
                pstmtCust.setString(2, lastName);
                pstmtCust.setString(3, email);
                pstmtCust.setString(4, phone);
                pstmtCust.executeUpdate();

                java.sql.ResultSet rs = pstmtCust.getGeneratedKeys();
                if (rs.next()) {
                    int customerId = rs.getInt(1);

                    // 5. Create their Bank Account automatically
                    String insertAccount = "INSERT INTO accounts (customer_id, account_type, balance) VALUES (?, ?, ?)";
                    java.sql.PreparedStatement pstmtAcc = conn.prepareStatement(insertAccount);
                    pstmtAcc.setInt(1, customerId);
                    pstmtAcc.setString(2, accountType);
                    pstmtAcc.setDouble(3, initialBalance);
                    pstmtAcc.executeUpdate();

                    javax.swing.JOptionPane.showMessageDialog(this, "Success! Created Customer ID: " + customerId);
                    
                   
                    loadTable();
                    clearFields();
                }
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for Initial Balance.");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tblCustomers.getSelectedRow();
        if (row == -1) { JOptionPane.showMessageDialog(this, "Select a row!"); return; }
        String id = tblCustomers.getValueAt(row, 0).toString();
        try (Connection conn = DatabaseConnection.getConnection()) {
            PreparedStatement pstmt = conn.prepareStatement("UPDATE customers SET first_name=?, last_name=?, email=?, phone_number=? WHERE customer_id=?");
            pstmt.setString(1, txtFirstName.getText());
            pstmt.setString(2, txtLastName.getText());
            pstmt.setString(3, txtEmail.getText());
            pstmt.setString(4, txtPhone.getText());
            pstmt.setString(5, id);
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Updated!");
            loadTable();
            clearFields();
        } catch (Exception e) { e.printStackTrace(); }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     
    int selectedRow = tblCustomers.getSelectedRow();
    
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please click on a customer in the table below to delete them.");
        return;
    }

   
    int customerId = Integer.parseInt(tblCustomers.getValueAt(selectedRow, 0).toString());

    
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to completely delete Customer " + customerId + "?\nThis will also permanently delete their bank accounts and transaction history!", 
            "Confirm Deletion", 
            javax.swing.JOptionPane.YES_NO_OPTION);
            
    if (confirm != javax.swing.JOptionPane.YES_OPTION) {
        return; 
    }

   
    try (java.sql.Connection conn = DatabaseConnection.getConnection()) {
        
      
        String getAccSql = "SELECT account_id FROM accounts WHERE customer_id = ?";
        java.sql.PreparedStatement pstmtAcc = conn.prepareStatement(getAccSql);
        pstmtAcc.setInt(1, customerId);
        java.sql.ResultSet rsAcc = pstmtAcc.executeQuery();
        
        while (rsAcc.next()) {
            int accountId = rsAcc.getInt("account_id");
            
          
            String delTransSql = "DELETE FROM transactions WHERE account_id = ?";
            java.sql.PreparedStatement pstmtTrans = conn.prepareStatement(delTransSql);
            pstmtTrans.setInt(1, accountId);
            pstmtTrans.executeUpdate();
        }

      
        String delAccSql = "DELETE FROM accounts WHERE customer_id = ?";
        java.sql.PreparedStatement pstmtDeleteAcc = conn.prepareStatement(delAccSql);
        pstmtDeleteAcc.setInt(1, customerId);
        pstmtDeleteAcc.executeUpdate();

       
        String delCustSql = "DELETE FROM customers WHERE customer_id = ?";
        java.sql.PreparedStatement pstmtDeleteCust = conn.prepareStatement(delCustSql);
        pstmtDeleteCust.setInt(1, customerId);
        pstmtDeleteCust.executeUpdate();

        javax.swing.JOptionPane.showMessageDialog(this, "Customer and all associated records deleted successfully!");
        
       
        loadTable();
        clearFields();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton3ActionPerformed

