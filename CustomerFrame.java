private void loadTable() {
 
        String[] columns = {"ID", "First Name", "Last Name", "Email", "Phone"};
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        try (Connection conn = DatabaseConnection.getConnection()) {
            ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM customers");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt("customer_id"), rs.getString("first_name"), rs.getString("last_name"), rs.getString("email"), rs.getString("phone_number")});
            }
            tblCustomers.setModel(model);
        } catch (Exception e) { e.printStackTrace(); }


}
